package com.rev.crr.demo.model;

public class Communication extends BaseDataService {

  public Communication() {
    super();
  }

  public Communication(String tittle, String number, String date) {
    super(tittle, number, date);
  }

  @Override
  public String getTittle() {
    return super.getTittle();
  }

  @Override
  public void setTittle(String tittle) {
    super.setTittle(tittle);
  }

  @Override
  public String getNumber() {
    return super.getNumber();
  }

  @Override
  public void setNumber(String number) {
    super.setNumber(number);
  }

  @Override
  public String getDate() {
    return super.getDate();
  }

  @Override
  public void setDate(String date) {
    super.setDate(date);
  }

  @Override
  public String toString() {
    return super.toString();
  }

  @Override
  public boolean equals(Object o) {
    return super.equals(o);
  }

  @Override
  public int hashCode() {
    return super.hashCode();
  }
}
